package io.spine.base;

import com.google.common.collect.ImmutableList;
import com.google.errorprone.annotations.Immutable;
import io.spine.protobuf.Messages;
import io.spine.type.KnownMessage;
import io.spine.validate.ConstraintViolation;
import io.spine.validate.MessageWithConstraints;

/**
 * A message for which {@link ValidatingBuilder} was generated by Spine Model Compiler.
 *
 * <p>Messages that implement this interface are <em>likely</em> produced by
 * {@link ValidatingBuilder#vBuild()} of the corresponding builder type. As such, these messages are
 * <em>likely</em> to {@linkplain #validate() conform} to the validation constraints defined in
 * the corresponding Protobuf type.
 *
 * <p>Implementing this interface does <em>NOT</em> guarantee such conformity.
 * This is so because an instance of a message can still be built by methods
 * {@link ValidatingBuilder#buildPartial()} or {@link ValidatingBuilder#build()} which do not
 * perform the check of the validation constraints.
 *
 * @param <B>
 *         the type of the builder
 * @param <M>
 *         the type of the message used for binging of the builder type
 */
@Immutable
public interface BuiltMessage<B extends ValidatingBuilder<M>, M extends BuiltMessage<B, M>>
        extends MessageWithConstraints, KnownMessage {

    /**
     * Obtains the instance of the builder for the passed message class.
     *
     * @param messageClass
     *          the class of the message to be created by the returned builder
     * @param <B>
     *          the type of the builder
     * @param <M>
     *          the type of the message
     * @return a new empty builder
     */
    @SuppressWarnings("unused")
    static <B extends ValidatingBuilder<M>, M extends BuiltMessage<B, M>>
    B builderOf(Class<? extends M> messageClass) {
        @SuppressWarnings("unchecked") // The cast is ensured by generic params of `BuildMessage`.
        B builder = (B) Messages.builderFor(messageClass);
        return builder;
    }

    @Override
    default ImmutableList<ConstraintViolation> validate() {
        //TODO:2021-01-30:alexander.yevsyukov: Remove when validating code is generated properly.
        return ImmutableList.of();
    }
}
